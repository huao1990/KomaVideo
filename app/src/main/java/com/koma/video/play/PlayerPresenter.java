package com.koma.video.play;

import android.support.annotation.NonNull;

import com.koma.video.data.VideosRepository;
import com.koma.video.util.LogUtils;

import javax.inject.Inject;

import io.reactivex.disposables.CompositeDisposable;

/**
 * Listens to user actions from the UI ({@link PlayerFragment}), retrieves the data and updates the
 * UI as required.
 * <p/>
 * By marking the constructor with {@code @Inject}, Dagger injects the dependencies required to
 * create an instance of the TasksPresenter (if it fails, it emits a compiler error).  It uses
 * {@link PlayerPresenterModule} to do so.
 * <p/>
 * Dagger generated code doesn't require public access to the constructor or class, and
 * therefore, to ensure the developer doesn't instantiate the class manually and bypasses Dagger,
 * it's good practice minimise the visibility of the class/constructor as much as possible.
 **/

public class PlayerPresenter implements PlayerContract.Presenter {
    private static final String TAG = PlayerPresenter.class.getSimpleName();

    @NonNull
    private PlayerContract.View mView;
    @NonNull
    private VideosRepository mRepository;

    private CompositeDisposable mDisposables;

    @Inject
    public PlayerPresenter(PlayerContract.View view, VideosRepository repository) {
        mView = view;

        mRepository = repository;

        mDisposables = new CompositeDisposable();
    }

    @Inject
    void setUpListener() {
        mView.setPresenter(this);
    }

    @Override
    public void subscribe() {
        LogUtils.i(TAG, "subscribe");
    }

    @Override
    public void unSubscribe() {
        LogUtils.i(TAG, "unSubscribe");

        mDisposables.clear();
    }
}
